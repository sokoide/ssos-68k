void ss_fill_long(volatile uint32_t* dst, uint32_t val, uint32_t count) {
    while (count >= 4) {
        *dst++ = val; *dst++ = val;
        *dst++ = val; *dst++ = val;
        count -= 4;
    }
    while (count--) *dst++ = val;
}

static void dma_fill_init(void) {
    dma_ch2->ccr = 0x00;
    dma_ch2->csr = 0xFF;
    dma_ch2->dcr = 0x08;
    dma_ch2->ocr = 0x99;
    dma_ch2->scr = 0x05;
    dma_ch2->mfc = 0x05;
    dma_ch2->dfc = 0x05;
}

void ss_dma_fill_setup(uint16_t value, int count) {
    for (int i = 0; i < count && i < 512; i++) {
        dma_fill_buf[i] = value;
    }
}

int ss_dma_fill_row(volatile uint16_t* dst, int count) {
    int timeout = 10000;
    xfr_table.mar = (uint8_t*)dma_fill_buf;
    xfr_table.mtc = (uint16_t)count;
    dma_ch2->csr = 0xFF;
    dma_ch2->dar = (uint8_t*)dst;
    dma_ch2->bar = (uint8_t*)&xfr_table;
    dma_ch2->btc = 1;
    dma_ch2->ccr = 0x80;

    while (!(dma_ch2->csr & 0x10) && --timeout > 0) {
        if (dma_ch2->csr & 0x02) {
            dma_ch2->csr = 0xFF;
            return -1;
        }
    }
    dma_ch2->csr = 0xFF;
    return (timeout > 0) ? 0 : -2;
}

void ss_gfx_init(void) {
    ss_draw_page = SS_GVRAM_PAGE0;
    ss_display_page = SS_GVRAM_PAGE0;
    ss_draw_idx = 0;
    ss_display_idx = 0;
    ss_gfx_clear(0);
}

void ss_gfx_flip(void) {
    ss_display_idx = ss_draw_idx;
    ss_draw_idx ^= 1;
    ss_draw_page = ss_draw_idx ? SS_GVRAM_PAGE1 : SS_GVRAM_PAGE0;
    ss_display_page = ss_display_idx ? SS_GVRAM_PAGE1 : SS_GVRAM_PAGE0;

    SS_CRTC_BASE[SS_CRTC_SCROLL_Y] = ss_display_idx ? (uint16_t)SS_SCREEN_H : 0;
}

void ss_gfx_clear(uint16_t color) {
    uint32_t c2 = ((uint32_t)color << 16) | color;
    uint32_t n = (uint32_t)(SS_PAGE_SIZE / 4);
    ss_fill_long((volatile uint32_t*)ss_draw_page, c2, n);
}

void ss_gfx_rect(int x, int y, int w, int h, uint16_t color) {
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > SS_SCREEN_W) w = SS_SCREEN_W - x;
    if (y + h > SS_SCREEN_H) h = SS_SCREEN_H - y;
    if (w <= 0 || h <= 0) return;

    uint32_t c2 = ((uint32_t)color << 16) | color;

    if (w > SS_DMA_FILL_THRESHOLD && h > 4) {
        ss_dma_fill_setup(color, w);
        dma_fill_init();
        int ok = 1;
        for (int row = y; row < y + h && ok; row++) {
            volatile uint16_t* b = ss_draw_page + row * (uint32_t)SS_SCREEN_W + x;
            if (ss_dma_fill_row(b, w) != 0) ok = 0;
        }
        dma_ch2->ccr = 0x00;
        if (ok) return;
    }

    for (int row = y; row < y + h; row++) {
        volatile uint16_t* b = ss_draw_page + row * (uint32_t)SS_SCREEN_W;
        int cx = x;
        if (cx & 1) {
            b[cx++] = color;
        }
        int n = (x + w - cx) / 2;
        ss_fill_long((volatile uint32_t*)(b + cx), c2, (uint32_t)n);
        if ((x + w - cx) & 1) {
            b[x + w - 1] = color;
        }
    }
}

void ss_gfx_hline(int x, int y, int w, uint16_t color) {
    ss_gfx_rect(x, y, w, 1, color);
}

void ss_gfx_fill_stipple(int x, int y, int w, int h, uint16_t c1, uint16_t c2) {
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > SS_SCREEN_W) w = SS_SCREEN_W - x;
    if (y + h > SS_SCREEN_H) h = SS_SCREEN_H - y;
    if (w <= 0 || h <= 0) return;

    int x1 = x + w - 1;
    int y1 = y + h - 1;

    uint32_t pat[2];
    pat[0] = ((uint32_t)c2 << 16) | c1;
    pat[1] = ((uint32_t)c1 << 16) | c2;

    for (int yy = y; yy <= y1; yy++) {
        volatile uint16_t* b = ss_draw_page + yy * (uint32_t)SS_SCREEN_W;
        int xx = x;
        if (xx & 1) {
            b[xx] = ((xx + yy) & 1) ? c1 : c2;
            xx++;
        }
        int n = (x1 - xx + 1) / 2;
        ss_fill_long((volatile uint32_t*)(b + xx), pat[yy & 1], (uint32_t)n);
        if ((x1 - xx + 1) & 1) {
            b[x1] = ((x1 + yy) & 1) ? c1 : c2;
        }
    }
}

void ss_gfx_char(int x, int y, char ch, uint16_t fg, uint16_t bg) {
    uint8_t c = (uint8_t)ch;
    if (c < 0x20 || c > 0x7E) c = ' ';
    const uint8_t* g = ss_font_data[c - 0x20];
    for (int r = 0; r < SS_FONT_H; r++) {
        int yy = y + r;
        if (yy < 0 || yy >= SS_SCREEN_H) continue;
        volatile uint16_t* row = ss_draw_page + yy * (uint32_t)SS_SCREEN_W;
        uint8_t bits = g[r];
        for (int b = 0; b < SS_FONT_W; b++) {
            int xx = x + b;
            if (xx >= 0 && xx < SS_SCREEN_W)
                row[xx] = (bits & (0x80 >> b)) ? fg : bg;
        }
    }
}

void ss_gfx_draw_text(int x, int y, const char* str, uint16_t fg, uint16_t bg) {
    while (*str) {
        ss_gfx_char(x, y, *str++, fg, bg);
        x += SS_FONT_ADV;
    }
}

void ss_gfx_char_clip(int x, int y, char ch, uint16_t fg, uint16_t bg,
                      const int* clip_wins, int nclip, int zpos) {
    uint8_t c = (uint8_t)ch;
    if (c < 0x20 || c > 0x7E) c = ' ';
    const uint8_t* g = ss_font_data[c - 0x20];
    for (int r = 0; r < SS_FONT_H; r++) {
        int yy = y + r;
        if (yy < 0 || yy >= SS_SCREEN_H) continue;
        volatile uint16_t* row = ss_draw_page + yy * SS_SCREEN_W;
        uint8_t bits = g[r];
        for (int b = 0; b < SS_FONT_W; b++) {
            int xx = x + b;
            if (xx < 0 || xx >= SS_SCREEN_W) continue;
            int covered = 0;
            for (int k = zpos + 1; k < nclip; k++) {
                const int* cw = &clip_wins[k * 4];
                if (xx >= cw[0] && xx < cw[0] + cw[2] &&
                    yy >= cw[1] && yy < cw[1] + cw[3]) {
                    covered = 1;
                    break;
                }
            }
            if (!covered)
                row[xx] = (bits & (0x80 >> b)) ? fg : bg;
        }
    }
}

void ss_gfx_draw_text_clip(int x, int y, const char* str, uint16_t fg, uint16_t bg,
                           const int* clip_wins, int nclip, int zpos) {
    while (*str) {
        ss_gfx_char_clip(x, y, *str++, fg, bg, clip_wins, nclip, zpos);
        x += SS_FONT_ADV;
    }
}
